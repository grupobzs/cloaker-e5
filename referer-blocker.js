// Função para verificar o Referer da requisição
function checkReferer() {
    const referer = document.referrer;
    console.log('Referer da requisição:', referer); // Log para depuração

    // Verificar se o Referer está na lista de URLs bloqueadas
    if (referer && blockedReferers.some(url => referer.startsWith(url))) {
        // Redirecionar se o Referer não for do seu domínio
        window.location.href = 'https://google.com'; // URL para redirecionar quando o Referer é bloqueado
    }
}

// Executar a verificação ao carregar a página
window.onload = checkReferer;
